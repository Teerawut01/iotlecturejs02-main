// Comparison Operators == === != !== > < >= <=
//ผลที่ได้มีแค่ true หรือ false หรือ 1 กับ 0 เท่านั้น
console.log("10" == 10)  // true
console.log("10" === 10)  //false /คำนึงถึงชนิด

console.log("Sombat" == "Sombat") //true
console.log("Sombat" === "Sombat") //true

console.log("Sombat" == "sombat") //false //ตัวใหญ่น้อยกว่าตัวเล็ก
console.log("Sombat" === "sombat") //false




// Logical Operators &&  ||  !
// เอา true ไปเป็น 1 และ false ไปเป็น 0
// และ ! คือ เปลี่ยน true ไปเป็น false และ false ไปเป็น true

// เช่น !true = false

// && = และ 
// true && true = true

// || = หรือ
// true || false = true

console.log(true && true) //true
console.log(true && false) //false
console.log(true || true) //true
console.log(true || false) //true
console.log(!true) //false
console.log(!false) //true

